/**
 * 查詢扣繳稅額
 * @param {number} income - 應收金額
 * @param {number} dependents - 扶養親屬人數 (0~11)
 * @returns {number} 應扣繳稅額
 */
export function lookupWithholdingTax(income, dependents) {
  if (!income || income <= 0) return 0;
  const dep = Math.min(Math.max(Math.round(dependents || 0), 0), 11);

  if (income < 80001) return 0;

  const idx = Math.floor((income - 80001) / 500);

  if (idx < TAX_ROWS.length) {
    return TAX_ROWS[idx][1 + dep] || 0;
  }

  // Linear extrapolation beyond table using last two rows
  const last = TAX_ROWS[TAX_ROWS.length - 1];
  const prev = TAX_ROWS[TAX_ROWS.length - 2];
  const slope = (last[1 + dep] - prev[1 + dep]) / 500;
  const bracketsBeyond = idx - (TAX_ROWS.length - 1);
  return Math.max(0, Math.round(last[1 + dep] + slope * bracketsBeyond * 500));
}
